export const getFilteredEvents = (filters) => {
  const { year, month } = filters;
  console.log(filters);
  const filteredEventsYear =
    year === "all"
      ? MOCK_EVENTS
      : MOCK_EVENTS.filter((event) => {
          let eventDate = new Date(event.date);
          console.log("year", eventDate.getFullYear());
          return eventDate.getFullYear().toString() === year;
        });
  const filteredEvents =
    month == "all"
      ? filteredEventsYear
      : filteredEventsYear.filter((event) => {
          let eventDate = new Date(event.date);
          return eventDate.getMonth().toString() === month;
        });
  return filteredEvents;
};
